package cpm.Loops;

public class ForLoopExample {

	public static void main(String[] args) {
	
		for (int i=1; i<=3; i++)
		{
		System.out.println(i+"Testing");
		}
		System.out.println("$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$");
		
		for (int j=1; j<=4; j=j+2)
		{
		System.out.println(j+"Selenium");
		}
		System.out.println("############################################");
		
		for (int hello=5; hello>=0; hello--)
		{
		System.out.println(hello+"Live Tech");
		}		
		System.out.println("*********************************************");
		
		for (int k=-5; k>-8; k--)
		{
		System.out.println("LoadRunner");
		}
		
		System.out.println("=======================================");
		
		for (int k=-5; k>=-7; k--)
		{
		System.out.println(k+"jmeter");
		}
		System.out.println("$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$");
		for (int l=-5; l<1; l=l+2)
		{
		System.out.println(l+"hi");
		}

	}

}
